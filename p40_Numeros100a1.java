
// Imprmir los numeros del 100 al 1 usando ciclo while

public class p40_Numeros100a1 {
    public static void main(String[] args) {
        int c;
        
        System.out.println("Iprimiendo  numeros del 100 al 1");
        System.out.println("");
        c=100;

        while( c >= 1 ) {
            System.out.print(c+" ");
            c = c - 1;
        }
        System.out.println("\nProceso terminado ...");
    }
    
}
